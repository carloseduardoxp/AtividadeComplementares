package model.domain;

public class Aluno {
	
	private String ra;
	
	private String nome;
	
	private PPC ppc;

	public String getRa() {
		return ra;
	}

	public void setRa(String ra) {
		this.ra = ra;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public PPC getPpc() {
		return ppc;
	}

	public void setPpc(PPC ppc) {
		this.ppc = ppc;
	}

}
